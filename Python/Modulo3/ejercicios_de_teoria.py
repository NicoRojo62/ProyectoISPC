# 3.1.1.2 Igualdad: el operador igual a (==)
var = 0 # asignando 0 a la variable var
print(var == 0) # El resultado seria True

var = 1        # asignando 1 a la variable var
print(var == 0) # El resultado seria False

# 3.1.1.2 Desigualdad: el operador no es igual (!=)
var = 0 # asignando 0 a la variable var
print(var != 0) # el resultado seria False

var = 1
print(var!=0) # el resultado seria True

